"""
COMENTÁRIOS EM PYTHON

• Comentários são trechos de texto inseridos no código com o objetivo de explicar 
  o funcionamento do programa para humanos — são ignorados pelo Python na execução.

• São úteis para:
  - Explicar trechos complexos do código.
  - Descrever o propósito de variáveis, funções, etapas, etc.
  - Indicar autoria e data de criação do script.
  - Marcar trechos de código temporariamente desativados (ex: durante testes).
  - Facilitar manutenção futura do código, inclusive pelo próprio autor.

• EM PYTHON:
  - Comentários começam com o símbolo # e vão até o fim da linha.
  - Para comentários em múltiplas linhas, repita o # em cada linha:

    # Comentário linha 1
    # Comentário linha 2

• Comentários podem estar sozinhos na linha ou ao lado de uma instrução:
    c = (a ** 2 + b ** 2) ** 0.5  # Calcula a hipotenusa

• Boas práticas:
  - Use nomes de variáveis claros e autoexplicativos (auto comentados):
    Ex: area_quadrado é melhor que a ou x.
  - Comente apenas quando necessário — comentários óbvios ou errados atrapalham.
  - Evite comentários desatualizados ou incorretos.

• Dica: Para comentar/descomentar várias linhas rapidamente:
  - Windows/Linux: Ctrl + /
  - Mac: Cmd + /

• Durante testes, é comum "comentar" linhas de código para isolar erros:
    # y = y + x  ← linha desativada temporariamente

• Bons comentários e nomes claros ajudam:
  - Outros programadores a entender seu código.
  - Você mesmo, no futuro, a lembrar como tudo funciona.

• Resumo:
  Comentários bem usados tornam seu código mais legível, fácil de manter e confiável.
  Use-os com sabedoria!
"""
